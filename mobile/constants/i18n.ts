const dictionaries: Record<SupportedLanguage, Dict> = { pl, uk, ru };

export const translate = (
  language: SupportedLanguage,
  key: string,
  vars?: Record<string, string | number>,
): string => {
  const dict = dictionaries[language] ?? dictionaries.pl;
  const extras = EXTRA_STRINGS[language] ?? EXTRA_STRINGS.pl;
  const raw =
    dict[key]
    ?? extras[key]
    ?? dictionaries.pl[key]
    ?? EXTRA_STRINGS.pl[key]
    ?? key;
  if (!vars) return raw;
  return Object.entries(vars).reduce(
    (acc, [k, v]) => acc.replaceAll(`{${k}}`, String(v)),
    raw,
  );
};
